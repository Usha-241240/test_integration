const daysOfWeek = [
  'Понедельник',
  'Вторник',
  'Среда',
  'Четверг',
  'Пятница',
  'Суббота',
  'Воскресенье',
];

function pickRandom(items, count) {
  const shuffled = [...items].sort(() => Math.random() - 0.5);
  return shuffled.slice(0, count);
}

function renderList(title, items) {
  const listItems = items.map((item) => `<li>${item}</li>`).join('');
  return `<h2>${title}</h2><ol>${listItems}</ol>`;
}

function renderContentPlan() {
  const topics = pickRandom(planTopics, 7);
  const days = daysOfWeek
    .map(
      (day, index) =>
        `<div class="plan-day"><strong>${day}</strong>${topics[index]}</div>`
    )
    .join('');
  return `<h2>Контент-план на неделю</h2>${days}`;
}

